    //Validate OTP
    /*
    - Ensure that the user has got an OTP
    - check if the otp hasn't expired
    - ensure the otp is correct
     */
    public AuthResponse validateOtp(OtpValidation otpValidationRequest){
        Otp otp = otpRepository.findByEmail(otpValidationRequest.getEmail());
        log.info("Email: {}", otpValidationRequest.getEmail());
        if(otp == null){
            return AuthResponse.builder()
                    .statusCode(400)
                    .responseMessage("You have not been sent an OTP")
                    .build();
        }
        if(otp.getExpiresAt().isBefore(LocalDateTime.now())){
            return AuthResponse.builder()
                    .statusCode(400)
                    .responseMessage("OTP has Expired")
                    .build();
        }
        if(!otp.getOtp().equals(otpValidationRequest.getOtp())){
            return AuthResponse.builder()
                    .statusCode(400)
                    .responseMessage("OTP invalid")
                    .build();
        }

        return AuthResponse.builder()
                .statusCode(200)
                .responseMessage("SUCCESS")
                .otpResponse(OtpResponse.builder()
                        .isOtpValid(true)
                        .build())
                .build();
    }
